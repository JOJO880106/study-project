let number = -6.7
//使用console.log除錯,安裝完外掛後可按f8開啟介面除錯
console.log(Math.abs(number)) //取絕對值:Math.abs()
console.log(Math.max(2,3,4,5,6,55,33,22)) //取最大值:Math.max()
console.log(Math.min(2,3,4,number,0)) //取最小值:Math.min()
console.log(Math.floor(number)) //取整數:Math.floor()
console.log(Math.round(number)) //四捨五入法:Math.round()
console.log(Math.pow(2,3)) //次方:Math.pow(底數,次方)
console.log(Math.sqrt(36)) //開根號:Math.sqrt()
console.log(Math.random()) //亂數:Math.random(),值為0~1隨機亂數可放大
console.log(Math.floor(Math.random()*10)) //亂數放大10倍再取整數